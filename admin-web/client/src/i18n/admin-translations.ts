export function resolveKey(lang: Lang, key: string): string {
  const parts = key.split('.');
  let node: any = dictionaries[lang];
  for (const p of parts) {
    node = node?.[p];
    if (node == null) return key;
  }
  return typeof node === 'string' ? node : key;
}
